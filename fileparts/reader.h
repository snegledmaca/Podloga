#ifndef READER_H_INCLUDED
#define READER_H_INCLUDED

//========================================================================
// Reader - dependancies
//========================================================================

    #include "../memoryparts/frompool.h"

//========================================================================
// Reader - definitions
//========================================================================

    class Reader;

//========================================================================
// Reader - implementations
//========================================================================

    class Reader: public FromPool {

    //====================================================================

    	public:

            char* holder;
            GLuint size;

    //====================================================================

    	public:

            virtual void reset( char* path ) {}

    		Reader( char* path = 0 ) {
    		    this->reset( path );
    		}

    		~Reader( void ) {}

    //====================================================================

    	public:

            virtual GLuint read_in( void ) = 0;

    //====================================================================
    // For Testing Purposes Only
    //====================================================================

    	public:

            void operator()( char* path ) {
                this->reset( path );

                char* data = 0;
                char t = 0;
                while( this->read_in() ) {
                    data = this->holder;
                    for ( GLuint i = 0; i < this->size; i++ ) {
                        t = data[i];
                    }
                    t++;
                }
            }

    //====================================================================

    };

//========================================================================

#endif // READER_H_INCLUDED
